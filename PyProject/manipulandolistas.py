# método index()
spaces = ['Church', 'House', 'Hotel', 'Motel', 'Park', 'Circle']
pos = spaces.index('Motel')
print(spaces)
print(f'O item desejado está na Posição: {pos}')
print('-='*35)
# ---------------------------------------------------------------------------
# método count()
players = ['Lebron', 'Curry', 'Damian', 'Curry', 'King Lebron', 'Kobe']
pos = players.count('Curry')
print(players)
print(f'O item desejado aparece: {pos}x na lista!!')
